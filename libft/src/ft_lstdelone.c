#include <stdlib.h>
#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (lst == NULLPTR)
		return ;
	if (del != NULLPTR)
		del(lst->token);
	free(lst);
}
